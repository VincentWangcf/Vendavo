package com.avnet.emasia.webquote.masterData.exception;

import com.avnet.emasia.webquote.exception.AppException;

/**
 * @author Tonmy,Li(906893)
 * @Created on 2013-01-22
 * 
 */
public class CheckedException extends AppException {

	private static final long serialVersionUID = 1L;

	public CheckedException() {
		super();
	}

	public CheckedException(String message) {
		super(message);
	}

	public CheckedException(Throwable e) {
		super(e);
	}

	public CheckedException(String message, Throwable cause) {
		super(message, cause);
	}

	public CheckedException(String errorCode, Object[] parameters) {
		super(errorCode, parameters);
	}

	public CheckedException(String errorCode, Throwable causedBy, Object[] parameters) {
		super(errorCode, causedBy, parameters);
	}
}
