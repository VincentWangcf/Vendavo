package com.avnet.emasia.webquote.masterData.dto;

import java.io.File;
import java.util.List;
/**
 * @author Tonmy,Li(906893)
 * @Created on 2013-01-25
 * 
 */
public class FileContext {

	String[] tables ;
    Config[] config;
    File[] files ;
    DataFile[][] dataFiles;
    ControlFile[][] controlFiles;
    List[][] dataFileList ;
    		
	
	
	public FileContext(String[] tables) {
		this.tables = tables;


	}

	public String[] getTables() {
		return tables;
	}

	public Config[] getConfig() {
		return config;
	}
	public void setConfig(Config[] config) {
		this.config = config;
	}
	public File[] getFiles() {
		return files;
	}
	public void setFiles(File[] files) {
		this.files = files;
	}
	public void setTables(String[] tables) {
		this.tables = tables;
	}

	public DataFile[][] getDataFiles() {
		return dataFiles;
	}

	public void setDataFiles(DataFile[][] dataFiles) {
		this.dataFiles = dataFiles;
	}

	public ControlFile[][] getControlFiles() {
		return controlFiles;
	}

	public void setControlFiles(ControlFile[][] controlFiles) {
		this.controlFiles = controlFiles;
	}

	public List[][] getDataFileList() {
		return dataFileList;
	}

	public void setDataFileList(List[][] dataFileList) {
		this.dataFileList = dataFileList;
	}
	
	
	
	
}
