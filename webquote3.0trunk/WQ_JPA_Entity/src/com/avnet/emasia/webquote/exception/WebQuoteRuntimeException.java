package com.avnet.emasia.webquote.exception;

/**
 * The Class WebQuoteRuntimeException.
 */
public class WebQuoteRuntimeException extends AbstractBaseRuntimeException {

	/** The Constant serialVersionUID. */
	private static final long serialVersionUID = -6387620855096632064L;

	/**
	 * Instantiates a new web quote runtime exception.
	 *
	 * @param errorCode the error
	 * @param causedBy the caused by
	 */
	public WebQuoteRuntimeException(final String errorCode, final Throwable causedBy) {
		super(errorCode, causedBy);
	}
	
	public WebQuoteRuntimeException() {
		super();
	}

	public WebQuoteRuntimeException(String errorCode, Object[] parameters) {
		super(errorCode, parameters);
	}

	public WebQuoteRuntimeException(String errorCode, Throwable causedBy, Object[] parameters) {
		super(errorCode, causedBy, parameters);
	}


	public WebQuoteRuntimeException(String errorCode) {
		super(errorCode);
	}

	public WebQuoteRuntimeException(Throwable causedBy) {
		super(causedBy);
	}

}
