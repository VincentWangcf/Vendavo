package com.avnet.emasia.webquote.webservice.drmsagprea.vo;

import java.math.BigDecimal;
import java.util.Date;

public class DrmsAgp
{

	private String zzProject;
	/*
	 * If MODE=’R’, it’s filled in by SAP;If MODE=’W’, it’s given by the calling system
	 */
    private BigDecimal authGpPercent;
	/*
	 * If MODE=’R’, it’s filled in by SAP;If MODE=’W’, it’s given by the calling system
	 */
    private String authGpChgRea;
	/*
	 * If MODE=’R’, it’s filled in by SAP;If MODE=’W’, it’s given by the calling system
	 */
    private String authGpChgRem;
	/*
	 * If MODE=’R’, it’s filled in by SAP;If MODE=’W’, it’s given by the calling system
	 */
    private Date expiryDate;
    /*
     * This is the user id provided by the calling system to identity who is actually performing this action through the calling system
     */
    private String upnam;
    
    /*
     * (R)ead or (W)rite
     */
    private String mode;
    
    
	public BigDecimal getAuthGpPercent()
	{
		return authGpPercent;
	}
	public void setAuthGpPercent(BigDecimal authGpPercent)
	{
		this.authGpPercent = authGpPercent;
	}
	public String getAuthGpChgRea()
	{
		return authGpChgRea;
	}
	public void setAuthGpChgRea(String authGpChgRea)
	{
		this.authGpChgRea = authGpChgRea;
	}
	public String getAuthGpChgRem()
	{
		return authGpChgRem;
	}
	public void setAuthGpChgRem(String authGpChgRem)
	{
		this.authGpChgRem = authGpChgRem;
	}
	public Date getExpiryDate()
	{
		return expiryDate;
	}
	public void setExpiryDate(Date expiryDate)
	{
		this.expiryDate = expiryDate;
	}
	public String getUpnam()
	{
		return upnam;
	}
	public void setUpnam(String upnam)
	{
		this.upnam = upnam;
	}
	public String getMode()
	{
		return mode;
	}
	public void setMode(String mode)
	{
		this.mode = mode;
	}
	public String getZzProject()
	{
		return zzProject;
	}
	public void setZzProject(String zzProject)
	{
		this.zzProject = zzProject;
	}
	@Override
	public String toString()
	{
		return "DrmsAgp [zzProject=" + zzProject + ", authGpPercent="
				+ authGpPercent + ", authGpChgRea=" + authGpChgRea
				+ ", authGpChgRem=" + authGpChgRem + ", expiryDate="
				+ expiryDate + ", upnam=" + upnam + ", mode=" + mode + "]";
	}
   
    
    
}
