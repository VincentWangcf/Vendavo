package com.avnet.emasia.webquote.edi.utils;

import java.util.HashMap;
import java.util.Map;

public class TIExchangeContext {
	private String key;
	private String className;
	private String templateFileName;
	private Map<String,String> entityMapFile =new HashMap<String,String>();
	private String xmlVerifyFileName;
	private int HeaderRowCount;
	
	
	public String getKey() {
		return key;
	}
	public void setKey(String key) {
		this.key = key;
	}
	public String getClassName() {
		return className;
	}
	public void setClassName(String className) {
		this.className = className;
	}
	public String getTemplateFileName() {
		return templateFileName;
	}
	public void setTemplateFileName(String templateFileName) {
		this.templateFileName = templateFileName;
	}
	public Map<String, String> getEntityMapFile() {
		return entityMapFile;
	}
	public void setEntityMapFile(Map<String, String> entityMapXml) {
		this.entityMapFile = entityMapXml;
	}
	public String getXmlVerifyFileName() {
		return xmlVerifyFileName;
	}
	public void setXmlVerifyFileName(String xmlVerifyFileName) {
		this.xmlVerifyFileName = xmlVerifyFileName;
	}
	public int getHeaderRowCount() {
		return HeaderRowCount;
	}
	public void setHeaderRowCount(int headerRowCount) {
		HeaderRowCount = headerRowCount;
	}
	
	
}
