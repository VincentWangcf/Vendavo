public class QuoteVo implements java.io.Serializable{
	
	private static final long serialVersionUID = -7515867380724829553L;

	private Quote quote;
	
	private Long total;
	
	private Long completed;
	
	private long attachCount;
	
	public QuoteVo(){
		
	}
	
	public QuoteVo(Quote quote){
		this.quote = quote;
	}

	public Quote getQuote() {
		return quote;
	}

	public void setQuote(Quote quote) {
		this.quote = quote;
	}

	public Long getTotal() {
		return total;
	}

	public void setTotal(Long total) {
		this.total = total;
	}

	public Long getCompleted() {
		return completed;
	}

	public void setCompleted(Long completed) {
		this.completed = completed;
	}

	public long getAttachCount() {
		return attachCount;
	}

	public void setAttachCount(long attachCount) {
		this.attachCount = attachCount;
	}

	

}
