package com.avnet.emasia.webquote.utilities.util;

import com.avnet.emasia.webquote.entity.Material;

public class MaterialHelper {
	/**If the SPECIAL_MATERIAL is   0001  , it means that it is special material.*/
	public static final String SPECIAL_MATERIAL = "0001";
	
	public static boolean isSpecialMaterial(Material material){
		if(material!=null&&material.getSpecialMaterial()!=null&&material.getSpecialMaterial().equals(SPECIAL_MATERIAL)){
			return true;
		}else{
			return false;
		}
	}

}
